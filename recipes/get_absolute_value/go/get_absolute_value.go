package main

import (
	"fmt"
	"math"
)

func main() {
	// Get absolute value of integer
	fmt.Println("Absolute value of  5 is ", math.Abs(5))
	fmt.Println("Absolute value of -5 is ", math.Abs(-5))

	// Get absolute value of float
	fmt.Println("Absolute value of  5.5 is ", math.Abs(5.5))
	fmt.Println("Absolute value of -5.5 is ", math.Abs(-5.5))
}
