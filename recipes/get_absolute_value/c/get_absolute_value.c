#include <stdio.h>
#include <math.h>

int main(void) {
  // Get absolute value of integer
  printf("Absolute value of  5 is %d\n", abs(5));
  printf("Absolute value of -5 is %d\n", abs(-5));

  // Get absolute value of float
  printf("Absolute value of  5.5 is %f\n", fabs(5.5));
  printf("Absolute value of -5.5 is %f\n", fabs(-5.5));

  return 0;
}
